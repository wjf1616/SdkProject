package com.chatsdk.model.mail.allianceapply;

public class AllianceApplyMailContents
{
	private String	applicantPic;
	private int		deal;
	private String	contents;
	private int		applicantPicVer;
	private String	applicantId;
	private int		applicantPower;
	private String	applicantName;

	public String getApplicantPic()
	{
		return applicantPic;
	}

	public void setApplicantPic(String applicantPic)
	{
		this.applicantPic = applicantPic;
	}

	public int getDeal()
	{
		return deal;
	}

	public void setDeal(int deal)
	{
		this.deal = deal;
	}

	public String getContents()
	{
		return contents;
	}

	public void setContents(String contents)
	{
		this.contents = contents;
	}

	public int getApplicantPicVer()
	{
		return applicantPicVer;
	}

	public void setApplicantPicVer(int applicantPicVer)
	{
		this.applicantPicVer = applicantPicVer;
	}

	public String getApplicantId()
	{
		return applicantId;
	}

	public void setApplicantId(String applicantId)
	{
		this.applicantId = applicantId;
	}

	public int getApplicantPower()
	{
		return applicantPower;
	}

	public void setApplicantPower(int applicantPower)
	{
		this.applicantPower = applicantPower;
	}

	public String getApplicantName()
	{
		return applicantName;
	}

	public void setApplicantName(String applicantName)
	{
		this.applicantName = applicantName;
	}

}

package com.chatsdk.util;

public class TranslateNewParams
{
	private String originalLang;
	private String translateMsg;
	private String targetLang;
	
	public String getOriginalLang()
	{
		return originalLang;
	}
	public void setOriginalLang(String originalLang)
	{
		this.originalLang = originalLang;
	}
	public String getTranslateMsg()
	{
		return translateMsg;
	}
	public void setTranslateMsg(String translateMsg)
	{
		this.translateMsg = translateMsg;
	}
	public String getTargetLang()
	{
		return targetLang;
	}
	public void setTargetLang(String targetLang)
	{
		this.targetLang = targetLang;
	}
	
	
}

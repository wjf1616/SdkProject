package com.chatsdk.model.mail.ocupy;

public class UserInfoParams
{

	private int		lv;
	private String	uid;
	private String	name;
	private String	pic;
	private int		picVer;

	public int getLv()
	{
		return lv;
	}

	public void setLv(int lv)
	{
		this.lv = lv;
	}

	public String getUid()
	{
		return uid;
	}

	public void setUid(String uid)
	{
		this.uid = uid;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public String getPic()
	{
		return pic;
	}

	public void setPic(String pic)
	{
		this.pic = pic;
	}

	public int getPicVer()
	{
		return picVer;
	}

	public void setPicVer(int picVer)
	{
		this.picVer = picVer;
	}

}
